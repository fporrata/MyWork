In one of the videos there is a library called dbplyr.  It is a library similar to dplyr but it is used with databases.  
I have not seen it before and wanted to give it a try.  I ran the queries in SQL and dbplyr.  
Found some interesting things with the basic test I did which I will comment at the end.

#--------- CODE ------------------------

library(DBI)
library(magrittr)
library(tidyverse)

#create new dataframe
location <- c("US", "Puerto Rico", "Egypt", "Mozambique", "Barbados", "Mexico")
longitude <- c(-98.35,-66.105,30.80,35.52, -59.54, -99.13)
latitude <- c(39.50,18.46,26.82,-25.95, 13.19, 19.43)

data511_loc <-data.frame(location, longitude, latitude)

con <- dbConnect(RSQLite::SQLite(), ":memory:")

#prepare table to use sql
dbWriteTable(con, "data511_loc", data511_loc)
dbListTables(con)

#prepare table to use dbplyr
data511_loc_dbplyr <- tbl(con, "data511_loc")

#SQL version
loc_result <- dbGetQuery(con, "SELECT location FROM data511_loc")
loc_result
#dbplyr version
loc_dbplyr <- data511_loc_dbplyr %>%
  select(location)
loc_dbplyr %>% collect()

#SQL Version
loc_lat_result <- dbGetQuery(con, "SELECT location, latitude FROM data511_loc where latitude < 0")
loc_lat_result

#dbplyr version
loc_lat_dbplyr_result <- data511_loc_dbplyr %>%
  select(location, latitude) %>%
  filter (latitude < 0)
loc_lat_dbplyr_result %>% collect()

#View SQL generated by dbplyr for the last query
loc_lat_dbplyr_result %>% show_query()

#This is a nice trick to learn SQL if you know how to use the dplyr functions

dbDisconnect(con)

