COMMENTS:

1 - Looks like the output from a SQL query is straight text and from dbplyr is a tibble.  A tibble is a "Souped up or suped up" version of a dataframe.

2 - The SQL query returned the exact latitude for Mozambique: -25.95.  The dbplyr query rounded to -26.  That could be a big problem.  Maybe there are some settings that I do not know of.

3 - In order to get the data from dbplyr you have to call collect().  This is call lazy loading

4 - This one is good or bad depending if you want to learn or if performance is important.  For dbplyr you can call show_query() to show the SQL if you want to learn SQL but the result it gave in this example I did not like.

The result should have been:

SELECT `location`, `latitude`
FROM `data511_loc`
WHERE (`latitude` < 0.0)  

which is a basic query, but it gave:

SELECT *
FROM (SELECT `location`, `latitude`
FROM `data511_loc`)
WHERE (`latitude` < 0.0)

It created a subquery inside the main query and this leads to bad performance.  Maybe there is a guideline provided to create better queries.
